public class variables {
    public static void main (String [] args){
        // variables
        // sum of a + b
        int a = 10;
        int b = 5;
        int sum = a + b;
        System.out.println(sum);
        
        // multiply 
        int mul = a * b;
        System.out.println(mul);
        
        // divide 
        int div = a / b;
        System.out.println(div);

        // subtraction 
        int sub =  a - b;
        System.out.println(sub);

    }
}
